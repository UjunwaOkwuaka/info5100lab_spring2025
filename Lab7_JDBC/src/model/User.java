public class User {

    //data members
    private int userId;
    private String name;
    private String college;
    private int age;
    private String hobby;
    

    
    //methods
    public int getUserId() {
        return userId;
    }

    public void setUserId(int userId) {
        this.userId = userId;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getCollege() {
        return college;
    }

    public void setCollege(String college) {
        this.college = college;
    }

    public int getAge() {
        return age;
    }

    public void setAge(int age) {
        this.age = age;
    }

    public String getHobby() {
        return hobby;
    }

    public void setHobby(String hobby) {
        this.hobby = hobby;
    }

    @Override
    public String toString() {
        return "User Profile Information:\n" + "userId= " + userId + "\n" + "name= " + name + "\n" +  "college= " + college + "\n" + "age= " + age + "\n" + " hobby= " + hobby + '}';
    }
}

    

    
